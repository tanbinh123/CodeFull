    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(this.KiemTra()){
            String str = "";
        str = "Họ" + txtho.getText()+"\n";
        str = "Tên" + txtten.getText()+"\n";
        str = "Bí danh" + txtbidanh.getText()+"\n";
        str = "Ghi chú" + txtghichu.getText()+"\n";
        
        if(rdonam.isSelected()){
            str = str + "Nam";
        }else if(rdonu.isSelected()){
            str = str + "Nữ";
        }else{
            str = str + "Không xác định";
        }
        str = str + "Quê quán" + cboque.getSelectedItem()+ "\n";
        if(chkan.isSelected()){
            str = str + "Sở Thích: ăn\n";
        }
        if(chkngunghi.isSelected()){
            str = str + "Sở Thích: Ngủ Nghỉ\n";
        }if(chknhay.isSelected()){
            str = str + "Sở Thích: Nhảy\n";
        }
        JOptionPane.showMessageDialog(this, str);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

