    public boolean KiemTra(){
        boolean check = true;
        if(txtho.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập họ");
            txtho.requestFocus();
        }else if(txtten.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên");
            txtten.requestFocus();
        }else if(txtten.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Hãy Nhập bí danh");
            txtbidanh.requestFocus();
        }else if(txtghichu.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Hãy Nhạp ghi chú");
            txtghichu.requestFocus();
        }
        return check;
    }
    
    

