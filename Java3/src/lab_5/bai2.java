    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(txtmasinhvien.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Nhập mã sinh viên");
            txtmasinhvien.requestFocus();
            return;
        }
        try {
            Connection con = DriverManager.getConnection(url, user,pass);
            String sql = "update sinhvien set tenSV=?, Email=?, SDT=?, diaChi=?, gioiTinh=? where maSV=?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, txthovaten.getText());
            st.setString(2, txtemail.getText());
            st.setString(3, txtsoDT.getText());
            st.setString(4, txtdiachi.getText());
            boolean gioiTinh;
            if(rdonam.isSelected()){
                gioiTinh = true;
            }else{
                gioiTinh = false;
            }
            st.setBoolean(5, gioiTinh);
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update thành công!");
            con.close();
            LoadDataToArray();;
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            Connection con = DriverManager.getConnection(url, user,pass);
            String sql = ("insert into sinhvien values(?,?,?,?,?,?)");
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, txtmasinhvien.getText());
            st.setString(2, txthovaten.getText());
            st.setString(3, txtemail.getText());
            st.setString(4, txtsoDT.getText());
            boolean gioiTinh;
            if(rdonam.isSelected()){
                gioiTinh = true;
            }else{
                gioiTinh = false;
                st.setBoolean(5, gioiTinh);
                st.setString(6, txtdiachi.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Save thành công");
                con.close();
                LoadDataToArray();
            }
        } catch (Exception e) {
            System.out.println("Lỗi: " + e);
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

