    public void Display(int i){
        SinhVien sv = list.get(i);
        txtdiachi.setText(sv.diaChi);
        txtemail.setText(sv.Email);
        txthovaten.setText(sv.hoTen);
        txtmasinhvien.setText(sv.Masv);
        txtsoDT.setText(sv.soDT);
        boolean gt = sv.gioiTinh;
        if(gt == true){
            rdonam.setSelected(true);
        }else{
            rdonu.setSelected(false);
        }
    }
    
    public void LoadDataToArray(){
        try {
            Connection con = DriverManager.getConnection(url, user,pass);
            Statement st = con.createStatement();
            String sql = "select * from sinhvien";
            ResultSet rs = st.executeQuery(sql);
            list.clear();
            while(rs.next()){
                String masv = rs.getString(1);
                String hoTen = rs.getString(2);
                String Email = rs.getString(3);
                String soDT = rs.getString(4);
                boolean gioiTinh = rs.getBoolean(5);
                String diaChi = rs.getString(6);
                SinhVien sv = new SinhVien(masv, hoTen, Email,soDT, diaChi, gioiTinh);
                list.add(sv);
            }
            con.close();
        } catch (Exception e) {
            System.out.println("Lỗi: "+e);
        }
    }

