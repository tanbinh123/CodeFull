     public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblsinhvien.getModel();
        model.setRowCount(0);
        for (SinhVien sv : list) {
            Object[] row = new Object[]{sv.getmasinhvien(), sv.getHoTen(), sv.getTuoi(), sv.isGioiTinh(),sv.getNganh()};
            model.addRow(row);
        }
    }
