    private void txtcreditKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcreditKeyTyped

        if (!Character.isDigit(evt.getKeyChar())) {
            evt.consume();
            this.getToolkit().beep();
        }
    }//GEN-LAST:event_txtcreditKeyTyped

    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        String input = txtcredit.getText().trim();
        int tong = 0;
        for (int i = 0; i < input.length(); i++) {
            tong += Integer.parseInt(String.valueOf(input.charAt(i)));
        }
        if (tong % 2 == 0) {
            JOptionPane.showMessageDialog(this, "tổng = " + tong + " và là số chẵn");
        } else {
            JOptionPane.showMessageDialog(this, "tổng = " + tong + " và là số lẻ");
        }

    }//GEN-LAST:event_btnCheckActionPerformed

    private void txtcreditKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcreditKeyReleased
        try {
            Integer.parseInt(txtcredit.getText());
        } catch (Exception e) {
            txtcredit.setText("");
        }
    }//GEN-LAST:event_txtcreditKeyReleased

