private boolean gioitinh() {
        if (rdonam.isSelected()) {
            return true;
        } else {
            return false;
        }
    }
private void xoatt() {
        txtma.setText(null);
        txtten.setText(null);
        txttuoi.setText(null);
        rdonam.setSelected(true);
        cbonganh.setSelectedItem(0);
        txtten.requestFocus();
    }
private void thoat() {
        int i = JOptionPane.showConfirmDialog(this, " Thoát Nha", "exit", 0);
        if (i == 0) {
            System.exit(0);
        }
    }
private void filltott(String a) {
        int o = Integer.valueOf(a) - 1;
        txtma.setText(list.get(o).getManv());
        txtten.setText(list.get(o).getHoten());
        txttuoi.setText(String.valueOf(list.get(o).getTuoi()));
        if (list.get(o).isGioitinh()) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        cbonganh.setSelectedItem(list.get(o).getNganh());
    }
private void save() {   
        try {
            File a = new File("E:\\giatan.txt");
            if(!a.exists()){
                a.createNewFile();
            }
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(a));
            oos.writeObject(list);            
            oos.close();
            JOptionPane.showMessageDialog(this, "Save thành công rồi nha ");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"lỗi rồi bạn ơi");
        }
    }
private void open() {
        try {
            try (ObjectInputStream is = new ObjectInputStream( new FileInputStream("E:\\giatan.txt"))) {
                list = (ArrayList<Nhanvien>) is.readObject();
            }
            lbltong.setText(String.valueOf(list.size()));
            JOptionPane.showMessageDialog(this, " MỞ file thành công");
        } catch (IOException | ClassNotFoundException e) {
            System.out.println("Lỗi rồi bạn ơi");
        }
    }
