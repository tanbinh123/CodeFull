    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);
        try {
            list = dao.select();
            for (Ve v : list) {
                Object[] row = {
                    v.getMaVe(),
                    v.getMaLichChieu(),
                    v.getMaGhe(),
                    v.getMaTaiKhoan(),
                    v.getNgayTao(),
                    v.getMaGiaVe(),
                    v.getGiaVe()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void search(String text) {
        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);
        try {
            for (Ve v : list) {
                if (v.getMaVe().contains(text)) {
                    Object[] row = new Object[]{
                        v.getMaVe(),
                        v.getMaLichChieu(),
                        v.getMaGhe(),
                        v.getMaTaiKhoan(),
                        v.getNgayTao(),
                        v.getMaGiaVe(),
                        v.getGiaVe()
                    };
                    model.addRow(row);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

