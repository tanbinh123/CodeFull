    LichChieu getModel() {
        LichChieu lc = new LichChieu();
        lc.setMaLichChieu(Integer.valueOf(lblMaLichChieu.getText()));
        lc.setNgayChieu(txtNgayChieu.getText());
        lc.setGioChieu((String) cboGioChieu.getSelectedItem());
        lc.setMaPhim(((Phim) cboPhim.getSelectedItem()).getMaLoaiPhim());
        lc.setMaPhongChieu(((PhongChieu) cboPhongChieu.getSelectedItem()).getMaPhongChieu());
        return lc;
    }

    private void setItemPhim() {
        List<Phim> list = pdao.select();
        for (Phim pm : list) {
            cboPhim.addItem(pm);
        }
    }

    private void setItemPhongChieu() {
        List<PhongChieu> list = pcdao.select();
        for (PhongChieu pc : list) {
            cboPhongChieu.addItem(pc);
        }
    }

    private void update() {
        LichChieu lc = getModel();
        try {
            dao.update(lc);
            this.dispose();
            this.jpanel.fillToTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private boolean check() {
        boolean KiemTra = false;
        String ngayChieu = txtNgayChieu.getText();
        String regexDate = "\\d{4}-\\d{1,2}-\\d{1,2}";
        if (ngayChieu.isEmpty()) {
            SonOptionPane.showMessage(super.getParent(), "KHÔNG ĐƯỢC ĐỂ TRỐNG!", SonOptionPane.WARNING);
            txtNgayChieu.requestFocus();
            KiemTra = false;
        } else if (!ngayChieu.matches(regexDate)) {
            SonOptionPane.showMessage(super.getParent(), "VUI LÒNG NHẬP ĐÚNG ĐỊNH DẠNG!", SonOptionPane.INFO);
            txtNgayChieu.requestFocus();
            KiemTra = false;
        } else {
            KiemTra = true;
        }
        return KiemTra;
    }

