    @Override
    public void actionAfterClick(ActionEvent e) {
        int row = table.getSelectedRow();
        int idLc = (int) table.getValueAt(row, 0);
        LichChieuDAO dao = new LichChieuDAO();
        LichChieu lc = dao.findByID(idLc);
        lblMaLichChieu.setText(String.valueOf(lc.getMaLichChieu()));
        txtNgayChieu.setText(lc.getNgayChieu());
        cboGioChieu.setSelectedItem(lc.getGioChieu());
        cboPhim.setSelectedItem(((Phim) cboPhim.getSelectedItem()).getMaPhim());
        cboPhongChieu.setSelectedItem(((PhongChieu) cboPhongChieu.getSelectedItem()).getMaPhongChieu());
        this.setVisible(true);
    }

